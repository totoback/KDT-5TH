let today = new Date().getDay();

if (today === 0) {
  alert("일요일");
} else if (today === 1) {
  alert("월요일");
} else if (today === 2) {
  alert("화요일");
} else if (today === 3) {
  alert("수요일");
} else if (today === 4) {
  alert("목요일");
} else if (today === 5) {
  alert("금요일");
} else if (today === 6) {
  alert("토요일");
}

